function upgrade_2_6_0_to_2_6_1()
{
    $nb_database = nbf_cms::$interop->database;
    $nb_database->skip_compat_processing = true;
    $text_to_replace = array();
    $replace_with = array();
    $text_to_add = array();
    $sql = array();

    //Don't default to payment plan 1 for orders created by an administrator
    $sql[] = "ALTER TABLE `#__nbill_orders` CHANGE `payment_plan_id` `payment_plan_id` INT( 11 ) UNSIGNED NOT NULL DEFAULT '0'";

    foreach ($sql as $query)
    {
        $nb_database->setQuery($query);
        $nb_database->query();
        if (nbf_common::nb_strlen($nb_database->_errorMsg) > 0)
        {
            nbf_globals::$db_errors[] = $nb_database->_errorMsg;
        }
    }
}
